local addr = {
   atSeed = 0x020EEF30,
   id1 = 0x020FB3F8,
   id2 = 0x02108FCC,
   playerx = 0x022BBC20,
   playery = 0x022BBC24,
   playerz = 0x022BBC28,
}

local seed

local function at_rand()
   local hi = bit.rshift(seed, 16)
   local lo = (bit.band(seed, 65535)) * 1103515245 + 12345
   local cr = bit.rshift(lo, 16)
   lo = bit.band(lo, 65535)
   hi = bit.band(hi * 1103515245 + cr, 65535)
   seed = bit.bor(bit.lshift(hi, 16), lo)
   return bit.band(hi, 32767)
end

local function main()
   local inputs = joypad.get(1)

   seed = memory.readdword(addr.atSeed)
   for i = 1, 33 do
      at_rand()
   end

   local newZone = at_rand() % #assetLocation + 1
   memory.writeword(addr.id1, assetLocation[newZone][1])
   memory.writeword(addr.id2, assetLocation[newZone][1])

   local x = memory.readdwordsigned(addr.playerx)
   local y = memory.readdwordsigned(addr.playery)
   local z = memory.readdwordsigned(addr.playerz)

   if inputs.A then
      y = y + 500
      memory.writedword(addr.playery, y)
   elseif inputs.B then
      y = y - 500
      memory.writedword(addr.playery, y)
   elseif inputs.start then
      memory.writedword(addr.playerx, 0x0)
      memory.writedword(addr.playery, 0x0)
      memory.writedword(addr.playerz, 0x0)
   end
   
end

gui.register(main)
